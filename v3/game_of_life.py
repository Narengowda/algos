len_x = len(x)
len_y = len(x[0])




def get_siblings(x, y):
    out = {}
    out["l"] = (x - 1, y) if (x - 1) >= 0 else None
    out["lt"] = (x - 1, y - 1) if ((x - 1) >= 0) and ((y - 1) >= 0) else None
    out["ld"] = (x - 1, y + 1) if ((x - 1) >= 0) and ((y + 1) <= len_y - 1) else None
    out["r"] = (x + 1, y) if (x + 1) <= len_x - 1 else None
    out["rt"] = (x + 1, y - 1) if ((x + 1) <= len_x - 1) and (y - 1 >= 0) else None
    out["rd"] = (
        (x + 1, y + 1) if ((x + 1) <= len_x - 1) and (y + 1 <= len_y - 1) else None
    )
    out["t"] = (x, y - 1) if (y - 1) >= 0 else None
    out["d"] = (x, y + 1) if (y + 1) <= len_y - 1 else None
    out["d"] = (x, y + 1) if (y + 1) <= len_y - 1 else None
    return list(out.values())


def logic(r, c):
    sibs = get_siblings(r, c)

    sibs_vals = [x[sib[0]][sib[1]] for sib in sibs if sib]
    alive_sibs = sibs_vals.count(1)

    if x[r][c]:
        # LIVE CELL
        # Any live cell with fewer than two live neighbors dies, as if caused by under-population.
        if alive_sibs < 2 or alive_sibs > 3:
            x[r][c] = 0
    else:
        # Inactive cell
        if alive_sibs == 3:
            x[r][c] = 1


def replace(i):
    if i:
        return "#"
    return "."


while True:
    for r in range(len_x):
        for c in range(len_y):
            logic(r, c)
        print([replace(i) for i in x[r]])

    time.sleep(1)
    sp.call("clear", shell=True)
